    public void closeAll(){
        if(auxCl == 1){
            panelGestionar.remove(clientes);
            auxCl = 0;
        }
        if(auxPd == 1){
            panelGestionar.remove(productos);
            auxPd = 0;
        }  
        if(auxPv == 1){
            panelGestionar.remove(proveedores);
            auxPv = 0;
        } 
        if(auxV == 1){
            panelGestionar.remove(ventas);
            auxV = 0;
        } 
        if(auxC == 1){
            panelGestionar.remove(compras);
            auxC = 0;
        }
    }
    
